public class Escape
{
    public static void main(String a[])
    {
        // \' is used to print single quotes

        System.out.println("Hello 'Java' \"Developer\"...");


        // \\ is used to print a single backslash

        System.out.println("Hello 'Java' \\Developer\\...");


        // \n moves the text to a new line

        System.out.println("Hello Java \nDeveloper...");


        // \t adds a horizontal tab space

        System.out.println("Hello Java\tDeveloper...");


        // \b removes one character before it (backspace)

        System.out.println("HTML\bDeveloper...");
    }
}
